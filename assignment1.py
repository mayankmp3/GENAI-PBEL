# Problem 1

def calculate_hra(basic_salary):
    return basic_salary * 0.20

def calculate_da(basic_salary):
    return basic_salary * 0.10

def calculate_net_salary(basic_salary):
    hra = calculate_hra(basic_salary)
    da = calculate_da(basic_salary)
    net_salary = basic_salary + hra + da
    return hra, da, net_salary

employee_name = input("Enter Employee Name: ")
basic_salary = float(input("Enter Basic Salary: "))

hra, da, net_salary = calculate_net_salary(basic_salary)

print("\n------ Employee Salary Details ------")
print("Employee Name :", employee_name)
print("Basic Salary  :", basic_salary)
print("HRA (20%)     :", hra)
print("DA (10%)      :", da)
print("Net Salary    :", net_salary)

