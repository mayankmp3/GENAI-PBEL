print("CSV file created successfully.\n")

products = pd.read_csv("products.csv")

print("Product Details:")
print(products)

average_price = np.mean(products["Price"])

print("\nAverage Price:", average_price)

def above_average_products(df, avg):
    print("\nProducts Priced Above Average:")

    for index, row in df.iterrows():
        if row["Price"] > avg:
            print(row["Product"], "-", row["Price"])

above_average_products(products, average_price)

