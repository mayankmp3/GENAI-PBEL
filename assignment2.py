import pandas as pd
import numpy as np

data = {
    "Day": ["Monday", "Tuesday", "Wednesday", "Thursday", "Friday", "Saturday", "Sunday"],
    "Temperature": [18, 24, 31, 29, 35, 19, 27]
}

df = pd.DataFrame(data)

df.to_csv("temperature.csv", index=False)

print("CSV file created successfully.\n")

temperature_data = pd.read_csv("temperature.csv")

print("Temperature Data:")
print(temperature_data)

highest = np.max(temperature_data["Temperature"])
lowest = np.min(temperature_data["Temperature"])
average = np.mean(temperature_data["Temperature"])

print("\nHighest Temperature :", highest, "°C")
print("Lowest Temperature  :", lowest, "°C")
print("Average Temperature :", average, "°C")

def classify_temperature(temp):
    if temp < 20:
        return "Cold"
    elif temp <= 30:
        return "Moderate"
    else:
        return "Hot"

print("\nTemperature Categories:")
for index, row in temperature_data.iterrows():
    category = classify_temperature(row["Temperature"])
    print(row["Day"], ":", row["Temperature"], "°C -", category)